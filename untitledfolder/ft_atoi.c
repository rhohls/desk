int	ft_atoi(const char *str)
{
	int ret;
	int i;

	i = 0;
	ret = 0;
	if ((str[0] == '-'))
		i++;
	while (str[i])
	{
		if ((str[i] < '0') || (str[i] > '9'))
			break;
		ret *= 10;
		ret += str[i] - 48;
		i++;
	}
	if (str[0] == '-')
		ret *= (-1);
	return (ret);
}
